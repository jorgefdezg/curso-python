mi_texto = "Hola"
mi_texto2 = "\"mundo\""

print(mi_texto,mi_texto2)

texto_unido = mi_texto + " " + mi_texto2
print(texto_unido)

#Salto de linea
texto_unido = mi_texto + " \n " + mi_texto2
print(texto_unido)

#Tabulacion
texto_unido = mi_texto + " \t" + mi_texto2
print(texto_unido)

#Borra todo lo anterior a la \r
texto_unido = mi_texto + " \r" + mi_texto2
print(texto_unido)