"""
Ejercicio 4.

Pedir dos numeros al usuario y hacer las operaciones basicas.
"""

numero1 = int(input("Escriba el primer numero con el que quiere operar: "))
numero2 = int(input("Escriba el segundo numero con el que quiere operar: "))

print(f"{str(numero1)} + {str(numero2)} = {numero1 + numero2}")
print(f"{str(numero1)} - {str(numero2)} = {numero1 - numero2}")
print(f"{str(numero1)} x {str(numero2)} = {numero1 * numero2}")
print(f"{str(numero1)} / {str(numero2)} = {numero1 / numero2}")
