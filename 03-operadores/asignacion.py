#Operadores de asignacion

edad = 55

print(edad)

edad +=5 #Es igual a edad = edad + 5
edad -=5 #Es igual a edad = edad -5
print(edad)

#Operadores incremento y decremento
year = 2021

#incremento
year += 1
print(year)

#decremento
year -= 1
print(year)

#Preincremento
year = 1 + year
print(year)

#Predrecemento
year = 1 - year
print(year)